#include "libft.h"

void	ft_strrev(char *s)
{
	int		i;
	int		len;

	i = 0;
	if (s)
	{
		len = ft_strlen(s) - 1;
		while (i < len)
		{
			ft_swap(&s[i], &s[len]);
			i++;
			len--;
		}
	}
}
