int		ft_sqrt(int n)
{
	int i;

	if (n <= 0)
		return (0);
	if (n == 1)
		return (1);
	if (n > 1)
	{
		i = 2;
		while (i <= (n / i))
		{
			if (i * i == n)
				return (i);
			i++;
		}
	}
	return (0);
}
