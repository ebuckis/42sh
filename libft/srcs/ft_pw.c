int		ft_pw(int nb, int power)
{
	if (power == 0)
		return (1);
	else if (power < 0 || nb == 0)
		return (0);
	else
	{
		if (power != 1)
			nb *= ft_pw(nb, power - 1);
		return (nb);
	}
}
