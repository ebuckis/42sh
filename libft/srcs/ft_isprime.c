int		ft_isprime(int n)
{
	int i;
	int c;

	i = 2;
	c = 0;
	if (n > 1)
	{
		while (i <= (n / i))
		{
			if (n % i == 0)
				c++;
			i++;
		}
		if (c == 0)
			return (1);
	}
	return (0);
}
