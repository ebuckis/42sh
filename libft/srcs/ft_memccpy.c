#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;
	char	*s1;
	char	*s2;

	i = 0;
	s1 = (char *)dst;
	s2 = (char *)src;
	while (n > 0)
	{
		s1[i] = s2[i];
		if (s2[i] == (char)c)
			return (dst + 1 + i);
		i++;
		n--;
	}
	return (NULL);
}
